package wuliu.entity;

public class OutStoreTask {
	private int outStoreTaskId;
	private int outStoreRequestId;
	private int storeId;
	private int stateId;
	public int getOutStoreTaskId() {
		return outStoreTaskId;
	}
	public void setOutStoreTaskId(int outStoreTaskId) {
		this.outStoreTaskId = outStoreTaskId;
	}
	public int getOutStoreRequestId() {
		return outStoreRequestId;
	}
	public void setOutStoreRequestId(int outStoreRequestId) {
		this.outStoreRequestId = outStoreRequestId;
	}
	public int getStoreId() {
		return storeId;
	}
	public void setStoreId(int storeId) {
		this.storeId = storeId;
	}
	public int getStateId() {
		return stateId;
	}
	public void setStateId(int stateId) {
		this.stateId = stateId;
	}
	
}
